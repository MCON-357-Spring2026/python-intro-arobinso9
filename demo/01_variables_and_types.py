# This script demonstrates variable assignment and data types in Python.
# Assigning different types of variables
#integer
age = 21
#string
name = "Alice"
#float
gpa = 3.75
#boolean
#only true and false in python. Not 0 or 1 like in Java.
is_active=True

#Printing variable values and their types
# type is a built-in function into python which tells us the variables type
print(age, type(age))
print(name, type(name))
print(gpa, type(gpa))
print(is_active, type(is_active))

age = "twenty one"
print(age, type(age))
